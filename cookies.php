		<h2>Informacja o ciasteczkach (cookies)</h2>
		<hr />
		<h3>Czym są ciasteczka?</h3>
		<p>Ciasteczka to niewielkie informacje tekstowe przechowywane w komputerze użytkownika. Wysyłane są poprzez serwer WWW, np. po odwiedzeniu jakiejś strony. Najczęściej są wykorzystywane w celu ułatwienia i zautomatyzowania obsługi witryny. Zawartość plików cookies nie pozwala na identyfikację użytkownika. Pliki cookies zazwyczaj zawierają nazwę strony internetowej, z której pochodzą, czas przechowywania na dysku twardym oraz unikalny numer.</p>
		<br />
		<h3>Zastosowanie ciasteczek</h3>
		<p>Pliki cookies wykorzystywane są do zapisywania drobnych danych użytkownika, co pozwala na automatyczne logowanie, zapamiętanie zawartości koszyka w sklepie internetowym czy też preferencji wyglądu, układu oraz języka witryny. Czasem są elementem liczników odwiedzin, sond oraz anonimowych statystyk, po to, by sprawdzić czy użytkownik z danego komputera został już zliczony przez licznik lub zagłosował w ankiecie.</p>
		<br />
		<h3>Blokowanie cookies</h3>
		<p>Aby zablokować pliki cookies lub uniemożliwić tylko niektóre funkcje, należy zmienić ustawienia swojej przeglądarki internetowej.</p>
